'''Задача 4. Напишите программу, которая будет преобразовывать десятичное число в двоичное.
- 45 -> 101101
- 3 -> 11
- 2 -> 10'''

N = int(input("Введите число для преобразовывания десятичного числа в двоичное:\n"))


def my_digit(n):
    j = []
    while n != 0:
        j.append(str(n % 2))
        n //= 2
    return "".join(j[::-1])


print(my_digit(N))

